/**
 * ARROW FUNCTIONS
 *
 * Functional Declaration (hoisted)
 * example:
 * function newFunc(para){
 *   // some code
 * }
 *
 * Function Expression (not hoisted, referred to as anonymous function)
 * example:
 * let tea = function(){
 *   // some code
 * }
 *
 *
 * Arrow Function (not hoisted, ES6, always anonymous function)
 * example:
 * let tea = () => {
 *   // some code
 * }
 *
 * With Arrow Functions, know BLOCK BODY (uses curly braces) v CONCISE BODY (NO curly braces or NO NEED to explicitly use RETURN keyword)
 *
 *
 */
