// IF CONDIIONALS

let isOn = true;

// Example of EXPLICITLY checking against true value
if (isOn === true) {
  console.log('The light is on');
}

// Example of IMPLICITLY checking true value
if (isOn) {
  console.log('The light is on, yay!');
}

let weather = 65;
if (weather < 70) {
  console.log('Wear a jacket!');
}

let string = 'Tyler';
if (string === 'Tyler' || false) {
  console.log('if statements and conditionals are powerful!!');
}
